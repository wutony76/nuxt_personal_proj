// 生成生肖數字
export function shengxiaoAll(animal) {
  const all = ['鼠', '牛', '虎', '兔', '龙', '蛇', '马', '羊', '猴', '鸡', '狗', '猪']
  if (!animal) return {}
  const pos = all.indexOf(animal)
  if (pos === -1) {
    console.log('無效的生肖', animal, pos)
    return {}
  }
  const tmp = [].concat(all.slice(0, pos + 1).reverse()).concat(all.slice(pos + 1).reverse())
  const ret = {}
  for (let i = 0; i < 4; i++) {
    for (let j = 0; j < 12; j++) {
      if (Array.isArray(ret[tmp[j]])) {
        const s = i * 12 + parseInt(ret[tmp[j]][0])
        ret[tmp[j]].push(String(s))
      } else {
        ret[tmp[j]] = []
        let t = j + 1
        t = t < 10 ? '0' + t : String(t)
        ret[tmp[j]].push(t)
      }
    }
  }
  ret[animal].push('49')
  return ret
}
